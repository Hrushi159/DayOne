package com.lti.basics;

public class NarrowingTypeCasting {

	public static void main(String[] args) {
     double d = 10.5;
     long l = (long)d;
     int a = (int)l;
     
     System.out.println("Double value "+d);
     System.out.println("long value "+l);
     System.out.println("Double value "+a);


	}

} 
